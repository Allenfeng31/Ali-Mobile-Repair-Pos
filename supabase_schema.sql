-- Supabase Database Schema
-- Run this script in the Supabase SQL Editor to create the necessary tables for the app.

-- 1. Inventory Table
CREATE TABLE public.inventory (
    id SERIAL PRIMARY KEY,
    name TEXT NOT NULL,
    model TEXT NOT NULL,
    brand TEXT,
    sku TEXT,
    stock INTEGER NOT NULL DEFAULT 0,
    "minStock" INTEGER NOT NULL DEFAULT 0,
    "costPrice" NUMERIC NOT NULL DEFAULT 0,
    price NUMERIC NOT NULL DEFAULT 0,
    margin NUMERIC NOT NULL DEFAULT 0,
    "iconName" TEXT NOT NULL DEFAULT 'Smartphone',
    status TEXT NOT NULL DEFAULT 'In Stock',
    category TEXT NOT NULL DEFAULT 'Uncategorized'
);

-- 2. Orders Table
CREATE TABLE public.orders (
    id TEXT PRIMARY KEY,
    timestamp TIMESTAMPTZ NOT NULL,
    subtotal NUMERIC NOT NULL DEFAULT 0,
    tax NUMERIC NOT NULL DEFAULT 0,
    total NUMERIC NOT NULL DEFAULT 0,
    profit NUMERIC NOT NULL DEFAULT 0,
    type TEXT NOT NULL,
    "paymentMethod" TEXT DEFAULT 'cash'
);

-- 3. Order Items Table
CREATE TABLE public.order_items (
    id TEXT PRIMARY KEY,
    order_id TEXT NOT NULL REFERENCES public.orders(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    sku TEXT NOT NULL,
    price NUMERIC NOT NULL,
    qty INTEGER NOT NULL,
    category TEXT NOT NULL
);

-- 4. Customers Table
CREATE TABLE public.customers (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    phone TEXT NOT NULL,
    email TEXT,
    "totalSpent" NUMERIC NOT NULL DEFAULT 0,
    status TEXT NOT NULL DEFAULT 'Active',
    "statusColor" TEXT NOT NULL DEFAULT 'bg-green-100 text-green-800',
    "lastVisit" TEXT,
    initials TEXT NOT NULL,
    "lastReviewSent" TIMESTAMPTZ
);

-- 5. Repairs Table
CREATE TABLE public.repairs (
    id TEXT PRIMARY KEY,
    customer_id TEXT NOT NULL REFERENCES public.customers(id) ON DELETE CASCADE,
    timestamp TIMESTAMPTZ NOT NULL,
    "repairItem" TEXT NOT NULL,
    "modelNumber" TEXT NOT NULL,
    price NUMERIC NOT NULL DEFAULT 0,
    "liquidDamage" BOOLEAN NOT NULL DEFAULT FALSE,
    password TEXT,
    imei TEXT,
    remark TEXT,
    status TEXT NOT NULL DEFAULT 'In Progress'
);

-- 6. Settings Table for global key-value settings (e.g. invoice header/footer)
CREATE TABLE public.settings (
    key TEXT PRIMARY KEY,
    value TEXT NOT NULL
);

